type CustomSidebarProps = React.ComponentProps<typeof Sidebar> & {
  setTitleSelected: React.Dispatch<React.SetStateAction<string>>
};

export function AppSidebar({ setTitleSelected, ...props }: CustomSidebarProps) {
  const [user, setUser] = useState({
    'name': "",
    'rol': "",
    'email': "",
    'avatar': "",
    'menu': ""
  })

  const [menu, setMenu] = useState([])

  useEffect(() => {
    fetch('/api/auth/me', { credentials: 'include' })
      .then(res => res.json())
      .then(data => {
        const m = JSON.parse(data.menu)
        setMenu(m.Data)
        setUser(data)
      })
      .catch((error) => {
        console.log("ðŸš€ ~ useEffect ~ error:", error)
      })
  },[])

  return (
    <Sidebar collapsible="offcanvas" {...props}>
      <SidebarHeader>
        <SidebarMenu>
          <SidebarMenuItem>
            <SidebarMenuButton
              asChild
              className="data-[slot=sidebar-menu-button]:!p-1.5"
            >
              <a href="#">
                <IconDental className="!size-5" />
                <span className="text-base font-semibold">Dental.</span>
              </a>
            </SidebarMenuButton>
          </SidebarMenuItem>
        </SidebarMenu>
      </SidebarHeader>
      <SidebarContent>
        <NavMain items={menu} setTitleSelected={setTitleSelected}/>
        {/* <NavDocuments items={data.documents} />
        <NavSecondary items={data.navSecondary} className="mt-auto" /> */}
      </SidebarContent>
      <SidebarFooter>
        <NavUser user={user} />
      </SidebarFooter>
    </Sidebar>
  )
}
